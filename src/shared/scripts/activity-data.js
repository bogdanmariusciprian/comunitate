// ---------------------------------------------------------
// Live helpers — the notifications badge counts only UNREAD received
// items and clears when the user visits "Activitatea mea"; commenting on
// someone else's post appends a real "given" entry.
// ---------------------------------------------------------

/** Unread received notifications (drives the badge). */
export function unreadActivityCount() {
  return ACTIVITY_RECEIVED.filter((a) => !a.read).length;
}

/** Mark everything received as read (called when the tab is opened) and
 *  remember it across reloads. */
export function markActivityRead() {
  for (const a of ACTIVITY_RECEIVED) a.read = true;
  try {
    localStorage.setItem(READ_KEY, "1");
  } catch {
    /* private mode — fine, in-memory flags still work */
  }
}

/** Record a comment/reply I just gave on someone else's content. */
export function recordGiven({ kind, action, snippet, context, postId = null }) {
  ACTIVITY_GIVEN.unshift(entry({ actorId: 0, agoMs: 0, kind, action, snippet, context, postId, read: true }));
}

/** A REAL event just landed in MY inbox (correct-mark, approval, mention…).
 *  Shows up unread in the bell + „Activitatea mea”. */
export function notifyReceived({ actorId, kind, action, snippet, context, postId = null, goSection = null }) {
  ACTIVITY_RECEIVED.unshift(entry({ actorId, agoMs: 0, kind, action, snippet, context, postId, goSection, read: false }));
}

/** Notify ANOTHER member (e.g. an @mention). In the mock only the current
 *  user's inbox exists, so other targets are a no-op — with Supabase this
 *  becomes an insert into the target's notifications. */
export function notifyUser(targetId, payload) {
  if (targetId === 0) notifyReceived(payload);
}
