// Mock "my activity" (no backend yet). Three streams shown in the
// community hub under "Activitatea mea":
//   - received: replies/comments others left on my posts or comments
//   - given:    comments I left on other people's posts
//   - followed: posts I chose to follow (pinned) — derived live from the
//               forum feed so toggling "urmărește" updates it instantly.
// Content is Romanian; identifiers stay English.
// =========================================================
import { userById, initials, avatarColor } from "./community-data.js";
import { relTime } from "./forum-data.js";

const MIN = 60 * 1000;
const HOUR = 60 * MIN;
const DAY = 24 * HOUR;

function person(id) {
  const u = userById(id) || { name: "Membru" };
  return { authorId: id, name: u.name, initials: initials(u.name), color: avatarColor(id) };
}

// "Seen" state survives reloads — otherwise the badge kept resurrecting
// after every full page load even though everything had been read.
const READ_KEY = "atelier_activity_read";
const seedsRead = typeof localStorage !== "undefined" && localStorage.getItem(READ_KEY) === "1";

// `postMatch` is a distinctive fragment of the source post's text — the hub
// resolves it to the live post so the row can link to where it happened.
// (Live entries carry a direct postId instead; `goSection` links a row to a
// hub section when there's no single post behind it, e.g. friend requests.)
function entry({ actorId, agoMs, kind, action, snippet, context, postMatch = null, postId = null, goSection = null, read = seedsRead }) {
  return { ...person(actorId), time: relTime(agoMs), kind, action, snippet, context, postMatch, postId, goSection, read };
}

